int main(void)
{
	u_int64_t n;
	int i, c;

	for (;;) {
		while ((c = getchar()) != EOF && !isdigit(c));
		if (c == EOF) break;
		for (n = 0; c != EOF && isdigit(c); c = getchar())
			n = n * 10 + (c - '0');
		if (n == 0) break;

		putchar('{');
		n--;
	
		for (i = 0, c = 0; i < 64; i++) {
			if (((n >> (u_int64_t)i) & 1) == 0)
				continue;

			printf(
				c++ ? ", %s" : " %s",
				powers3[i]
			);
		}
		printf(" }\n");
	}

	return 0;
}
