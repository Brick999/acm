static int f()
{
	int c, i, l, x;
	unsigned long d;

	for (i=0,l=3,d=0,x=0;;) {
		c = getchar();
		if (c == EOF || c == '#')
			break;

		if (c != '=') {
			if ((c = b64tab[(unsigned char)c]) == 64)
				continue;
			d |= c;
		} else {
			l--;
		}
	
		i++;
		x++;

		if (i == 4) {
			while (l-- > 0) {
				putchar((d >> 16) & 0xFF);
				d <<= 8;
			}
			i = 0;
			l = 3;
			d = 0;
		} else {
			d <<= 6;
		}

	}

	return x;
}

int main()
{
	while (f() > 0)
		putchar('#');
	return 0;

}
