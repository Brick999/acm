void makesieve()
{
	static int sieve[131072];
	int i, j;

	memset(sieve, 0, sizeof(sieve));
	memset(primesum, 0, sizeof(primesum));

	for (i = 2; i < 1000; i++)
		if (sieve[i] == 0)
			for (j = i * i; j < 100000; j += i) sieve[j] = 1;

	for (i = 10000; i <= 99999; i++) {
		if (sieve[i])
			continue;

		for (primesum[i] = 0, j = i; j > 0; j /= 10)
			primesum[i] += j % 10;
	}
}

void print(char *a, int s)
{
	static int prim[1024], nprim, m[8][8];
	int i, j, k, n;

	for (nprim = 0, i = 10000; i <= 99999; i++)
		if (primesum[i] == s) prim[nprim++] = i;

	for (n = 0; *a; n++) {
		if (n != 0)
			printf("\n");

		for (i = 1; i <= 4; i++, a += 2) {
			k = prim[al[a[0]] * 36 + al[a[1]]];
			printf("%d\n", k);
			for (j = 5; j >= 1; j--, k /= 10)
				m[i][j] = k % 10;
		}

		for (i = 1; i <= 5; i++)
			for (m[5][i] = s, j = 1; j <= 4; j++)
				m[5][i] -= m[j][i];

		for (i = 1, j = 0; i <= 5; i++)
			j = j * 10 + m[5][i];

		printf("%d\n", j);
	}
}

